int	ft_str_is_numeric(char *str)
{
	int	i;
	int	aux;

	i = 0;
	if (str[i] == '\0')
	{
		return (1);
	}
	while (str[i] != '\0')
	{
		if (str[i] > 47 && str[i] < 58)
		{
			aux = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (aux);
}
